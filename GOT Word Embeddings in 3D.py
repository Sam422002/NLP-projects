# ğŸš« Import predefined list of English stopwords (common words like 'the', 'and', etc.) from Gensim
from gensim.parsing.preprocessing import STOPWORDS

# ğŸ§¾ Display the set of stopwords
STOPWORDS

# ğŸ“– Import NLTK's sentence tokenizer
from nltk.tokenize import sent_tokenize

# ğŸ§¹ Import Gensim's text cleaner - lowercases, tokenizes, removes punctuation, numbers, etc.
from gensim.utils import simple_preprocess

# ğŸ“ List of file paths containing text data (e.g., Game of Thrones scripts)
file_paths = [
    "/content/001ssb.txt",
    "/content/002ssb.txt",
    "/content/003ssb.txt",
    "/content/004ssb.txt",
    "/content/005ssb.txt"
]

# ğŸ“š Load the content of all text files into a list
all_texts = []
for path in file_paths:
    with open(path, 'r', encoding='cp1252') as f:  # cp1252 encoding for special characters
        all_texts.append(f.read())

# ğŸ§½ Preprocessing: Sentence tokenization + word tokenization + stopword removal
cleaned_sentences = []
for text in all_texts:
    sentences = sent_tokenize(text)  # Break full text into sentences
    for sentence in sentences:
        tokens = simple_preprocess(sentence)  # Tokenize each sentence into lowercase words
        clean_tokens = [word for word in tokens if word not in STOPWORDS]  # Remove stopwords
        if clean_tokens:  # Ignore empty or meaningless lines
            cleaned_sentences.append(clean_tokens)

# ğŸ§  Train a Word2Vec model on the cleaned and tokenized sentences
model = gensim.models.Word2Vec(
    sentences=cleaned_sentences,  # List of tokenized sentences
    vector_size=100,              # Size of word vectors (default = 100)
    window=10,                    # Context window size
    min_count=2,                  # Ignores words that appear fewer than 2 times
    workers=4,                    # Use 4 CPU cores for faster training
    sg=1,                         # 1 = Skip-gram; 0 = CBOW
    epochs=25                     # Number of training iterations
)

# ğŸ”§ Builds the vocabulary (dictionary of unique words and counts) from the tokenized sentences
model.build_vocab(cleaned_sentences)

model.train(cleaned_sentences , total_examples = model.corpus_count , epochs = 25)

model.wv.most_similar('daenerys')  # ğŸ” Finds the top 10 words most similar to 'daenerys' based on cosine similarity from the trained Word2Vec model

